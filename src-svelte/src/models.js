// Helper function to get models by type and location
export function getModelsByType(modelType) {
	const allModels = {
		model: mockModels,
		lora: mockLoras,
		control: mockControlNets,
	};

	const models = allModels[modelType] || [];

	const macModels = models
		.filter((m) => m.exists_mac_hd)
		.sort((a, b) => (a.mac_display_order || 0) - (b.mac_display_order || 0));

	const stashModels = models
		.filter((m) => m.exists_stash && !m.exists_mac_hd)
		.sort((a, b) => {
			const aName =
				a.display_name || a.display_name_original || a.filename || "";
			const bName =
				b.display_name || b.display_name_original || b.filename || "";
			return aName.localeCompare(bName);
		});

	return { macModels, stashModels };
}

// Format file size for display
export function formatFileSize(bytes) {
	if (!bytes) return "Unknown";
	if (bytes < 1024) return bytes + " B";
	if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + " KB";
	if (bytes < 1024 * 1024 * 1024)
		return (bytes / (1024 * 1024)).toFixed(1) + " MB";
	return (bytes / (1024 * 1024 * 1024)).toFixed(1) + " GB";
}

// Get display name for a model
export function getDisplayName(model) {
	return model.display_name || model.display_name_original || model.filename;
}
